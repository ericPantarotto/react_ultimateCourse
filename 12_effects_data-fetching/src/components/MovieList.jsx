import PropTypes from 'prop-types';

// Stateful
export function MovieList({movies}) {
  return (
    <ul className='list'>
      {movies?.map((movie) => (
        <Movie movie={movie} key={movie.imdbID} />
      ))}
    </ul>
  );
}
MovieList.propTypes = {
  movies: PropTypes.array,
};


